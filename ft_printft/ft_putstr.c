#include "ft_printf.h"

void	ft_putstr(char *s, size_t *count)
{
	size_t	i;

	i = 0;
	if (s == NULL)
		s = "(null)";
	while (s[i] != '\0')
	{
		write(1, &s[i], 1);
		(*count)++;
		i++;
	}
}
