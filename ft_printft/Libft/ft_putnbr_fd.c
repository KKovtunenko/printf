#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long int	nb;
	char		k;

	nb = n;
	if (nb < 0)
	{
		write (fd, "-", 1);
		nb = -nb;
	}
	if (nb >= 10)
	{
		ft_putnbr_fd(nb / 10, fd);
	}
	k = (nb % 10) + '0';
	write(fd, &k, 1);
}
